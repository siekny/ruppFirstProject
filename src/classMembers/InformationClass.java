package classMembers;

public class InformationClass {
	private String date;
	private String action;
	
	public InformationClass(String date, String action) {
		this.date = date;
		this.action = action;
	}
	
	public String getDate() {
		return date;
	}
	
	public String getAction() {
		return action;
	}
}
